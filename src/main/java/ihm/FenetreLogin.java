    private void jButtonSeConnecterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeConnecterActionPerformed
        String login = jTextFieldLogin.getText();
    String motDePasse = new String(jPasswordFieldMotDePasse.getPassword());

    if (login.isEmpty() || motDePasse.isEmpty()) {
        jLabelMessage.setText("Veuillez remplir tous les champs");
        return;
    }

    UtilisateurDAO utilisateurDAO = new UtilisateurDAO();
    Utilisateur utilisateur = utilisateurDAO.authentifier(login, motDePasse);

    // ‚ùå Authentication failed
    if (utilisateur == null) {
        jLabelMessage.setText("Login ou mot de passe incorrect");
        return;
    }

    // ‚úÖ Authentication success
    jLabelMessage.setText(
        "Bienvenue " + utilisateur.getLogin() +
        " (" + utilisateur.getRole() + ")"
    );

    // üéØ Role-based navigation
    if (utilisateur.getRole() == RoleUtilisateur.ETUDIANT) {
        new FenetreEtudiant(utilisateur).setVisible(true);

    } else if (utilisateur.getRole() == RoleUtilisateur.ENSEIGNANT) {
        new FenetreEnseignant().setVisible(true);
    }

    // Close login window
    this.dispose();
    }//GEN-LAST:event_jButtonSeConnecterActionPerformed

