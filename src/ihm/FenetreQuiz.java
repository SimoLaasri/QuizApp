private void chargerQuestions() {
    QuizDAO quizDAO = new QuizDAO();
    Quiz quizTrouve = null;

    for (Quiz q : quizDAO.recupererListQuiz()) {
        if (q.getTitre().equals(titreQuiz)) {
            quizTrouve = q;
            break;
        }
    }

    if (quizTrouve != null) {
        questions = quizTrouve.getQuestions();
        quizId = quizTrouve.getId();
    } else {
        questions = new java.util.ArrayList<>();
    }

    indexQuestion = 0;
    score = 0;
}


private void afficherQuestion() {
    if (questions == null || questions.isEmpty()) {
        labelQuestion.setText("Aucune question disponible pour ce quiz.");
        radioA.setEnabled(false);
        radioB.setEnabled(false);
        radioC.setEnabled(false);
        radioD.setEnabled(false);
        return;
    }

    if (indexQuestion >= questions.size()) {
        labelQuestion.setText("Quiz terminé.");
        radioA.setEnabled(false);
        radioB.setEnabled(false);
        radioC.setEnabled(false);
        radioD.setEnabled(false);
        return;
    }

Question q = questions.get(indexQuestion);

labelQuestion.setText(q.getEnonce());

List<String> choix = q.getChoix();

radioA.setText(choix.get(0));
radioB.setText(choix.get(1));
radioC.setText(choix.get(2));
radioD.setText(choix.get(3));


    buttonGroup1.clearSelection();
    
    // Restore previously selected answer if exists
    if (reponses != null && indexQuestion >= 0 && indexQuestion < reponses.length && reponses[indexQuestion] != -1) {
        switch (reponses[indexQuestion]) {
            case 0: radioA.setSelected(true); break;
            case 1: radioB.setSelected(true); break;
            case 2: radioC.setSelected(true); break;
            case 3: radioD.setSelected(true); break;
        }
    }
    
    mettreAJourBoutons();
}

