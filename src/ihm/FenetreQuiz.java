    private void btnSuivantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuivantActionPerformed
        // Save current answer
        sauvegarderReponse();
        
        if (indexQuestion >= questions.size() - 1) {
            // Last question - show confirmation dialog
            int confirmation = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Voulez-vous terminer le quiz ?",
                "Confirmation",
                javax.swing.JOptionPane.YES_NO_OPTION
            );
            
            if (confirmation == javax.swing.JOptionPane.YES_OPTION) {
                calculerScore();
                afficherResultats();
            }
        } else {
            indexQuestion++;
            afficherQuestion();
        }
    }//GEN-LAST:event_btnSuivantActionPerformed

    private void btnPrecedentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrecedentActionPerformed
        // Save current answer before going back
        sauvegarderReponse();
        
        indexQuestion--;
        afficherQuestion();
    }//GEN-LAST:event_btnPrecedentActionPerformed
    
    private void sauvegarderReponse() {
        if (indexQuestion >= 0 && indexQuestion < questions.size() && reponses != null && indexQuestion < reponses.length) {
            if (radioA.isSelected()) reponses[indexQuestion] = 0;
            else if (radioB.isSelected()) reponses[indexQuestion] = 1;
            else if (radioC.isSelected()) reponses[indexQuestion] = 2;
            else if (radioD.isSelected()) reponses[indexQuestion] = 3;
            else reponses[indexQuestion] = -1;
        }
    }
    
    private void mettreAJourBoutons() {
        // Disable "Précédent" on first question
        btnPrecedent.setEnabled(indexQuestion > 0);
        
        // Change "Suivant" to "Terminer" on last question
        if (indexQuestion >= questions.size() - 1) {
            btnSuivant.setText("Terminer");
        } else {
            btnSuivant.setText("Suivant");
        }
    }
    
    private void calculerScore() {
        score = 0;
        for (int i = 0; i < questions.size(); i++) {
            if (reponses[i] == questions.get(i).getBonneReponse()) {
                score++;
            }
        }
    }

    private void afficherResultats() {
        // Stop timer when showing results
        arreterTimer();
        
        int totalQuestions = questions.size();
        double pourcentage = totalQuestions > 0 ? (score * PERCENTAGE_MULTIPLIER / totalQuestions) : 0;
        
        String message = String.format(
            "Quiz terminé!\n\nScore: %d/%d (%.1f%%)",
            score, totalQuestions, pourcentage
        );
        
        javax.swing.JOptionPane.showMessageDialog(
            this,
            message,
            "Résultats du Quiz",
            javax.swing.JOptionPane.INFORMATION_MESSAGE
        );
        
        // Save attempt to database
        if (utilisateurConnecte != null && quizId > 0) {
            TentativeDAO tentativeDAO = new TentativeDAO();
            tentativeDAO.ajouterTentative(utilisateurConnecte.getId(), quizId, score);
        }
        
        this.dispose();
    }

    /**
     * Start the countdown timer for the quiz
     */
    private void demarrerTimer() {
        // Create a timer that fires every second (1000 ms)
        timer = new javax.swing.Timer(1000, e -> {
            tempsRestantSecondes--;
            mettreAJourAffichageTimer();
            
            // Show warning when 30 seconds remain
            if (tempsRestantSecondes == WARNING_THRESHOLD_SECONDS && !warningShown) {
                warningShown = true;
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Attention ! Il vous reste 30 secondes pour terminer le quiz !",
                    "Avertissement",
                    javax.swing.JOptionPane.WARNING_MESSAGE
                );
            }
            
            // Auto-submit when time runs out
            if (tempsRestantSecondes <= 0) {
                arreterTimer();
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Temps écoulé ! Le quiz est terminé automatiquement.",
                    "Fin du temps",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE
                );
                calculerScore();
                afficherResultats();
            }
        });
        
        mettreAJourAffichageTimer();
        timer.start();
    }
    
    /**
     * Stop the timer
     */
    private void arreterTimer() {
        if (timer != null && timer.isRunning()) {
            timer.stop();
        }
    }
    
    /**
     * Update the timer display label
     */
    private void mettreAJourAffichageTimer() {
        int minutes = tempsRestantSecondes / 60;
        int secondes = tempsRestantSecondes % 60;
        String texteTimer = String.format("Temps restant: %02d:%02d", minutes, secondes);
        labelTempsRestant.setText(texteTimer);
        
        // Change color to red when less than or equal to 30 seconds remain
        if (tempsRestantSecondes <= WARNING_THRESHOLD_SECONDS) {
            labelTempsRestant.setForeground(new java.awt.Color(255, 0, 0));
        } else {
            labelTempsRestant.setForeground(new java.awt.Color(0, 102, 204));
        }
    }


